# ___________________________________________________
# type()   builtin functions show the type of any object
# all data in python is object
# ___________________________________________________
print(type(10))  #int => Integer
print(type(-10)) #int => Integer

print(type(1.2)) #float => Float

print(type("Hello")) #str => String

print(type(True)) #bool => Boolean

print(type([1,2,3,4,5,6])) #list => List

print(type((1,2,3)))  #tuple => Tuple

print(type({"one":1,"two":2})) #dict => Dictionary

print(type(None)) #NoneType => None

